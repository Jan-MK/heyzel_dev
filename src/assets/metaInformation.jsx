// TODO LANGUAGES FOR COUNTRIES in JOB FORM
export function generateSeo(identifier, language) {
    let seoData = metaData[identifier];
    if (!seoData) {
        seoData = metaData["home"];
    }
    return (
        <Helmet>
            <html lang={language}/>
            <title>{seoData.title[language]}</title>
            <meta name="description" content={seoData.description[language]}/>
            <meta name="keywords" content={seoData.keywords[language]}/>
            <link rel="icon" href={metaData.image}/>

            {/* Open Graph / Facebook */}
            <meta property="og:type" content="website"/>
            <meta property="og:url" content={seoData.url[language]}/>
            <meta property="og:title" content={seoData.title[language]}/>
            <meta property="og:description" content={seoData.description[language]}/>
            <meta property="og:image" content={metaData.image}/>

            {/* Twitter */}
            <meta property="twitter:url" content={seoData.url[language]}/>
            <meta property="twitter:title" content={seoData.title[language]}/>
            <meta property="twitter:description" content={seoData.description[language]}/>
            <meta property="twitter:image" content={metaData.image}/>
        </Helmet>
    );

}
